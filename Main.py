# 循环让用户选择问题并回答
def ask_question():
    while True:
        print("\n请选择一个问题 (输入数字 1 到 2)：")
        for i, q in enumerate(questions, 1):
            print(f"{i}. {q['question']}")
        print("0. 退出")
        
        try:
            choice = int(input("你的选择："))
        except ValueError:
            print("请输入一个有效的数字！")
            continue
        
        if choice == 0:
            print("感谢使用！再见！")
            break
        
        if 1 <= choice <= len(questions):
            question = questions[choice - 1]
            print(f"\n问题：{question['question']}")
            print(f"回答：{question['answer']}")
            print(question['ascii_art'])
        else:
            print("无效的选择，请重新选择！")

# 启动程序
ask_question()
